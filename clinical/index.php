			<p class="text">Each study's primary end-point was to evaluate the test articles for safety by: a) determining if any of the product’s metabolites were being absorbed through the skin, b) and to what extent, c) if there were any noticeable changes in vital signs, and d) if there were changes in blood chemistry or organ system functions. In addition, subjects were observed for any skin sensitivity or adverse events related to the test product.</p>

			<p>The result of the study showed that all subjects had no deviation from their normal ranges. In addition, there were no detectable metabolites absorbed into the body system, nor were any adverse reactions noted. With regard to efficacy, ST&amp;T was surprised to find that a statistically significant number of subjects reported positive results to their skin health, considering the fact that the test articles contained no "additives or cleansing compounds" that are commonly found in other products.</p> 

