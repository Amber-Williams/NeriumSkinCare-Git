<h1 class="newsTopTitle">FROM THE NERIUMAD PRODUCT MANUFACTURER<br />IMPORTANT NEWS FOR OUR CUSTOMERS</h1>

		<p>Nerium Skincare, Inc. Files Several Lawsuits Against its Partner &amp; Distributor Nerium International.</p>

		<p>Nerium SkinCare Inc., and its parent company Nerium Biotechnology Inc., have initiated litigation against its distributor Nerium International (NI) which was originally founded and established by Nerium SkinCare Inc., in partnership with Jeff Olson Products. </p>

		<p>The intent of Nerium SkinCare Inc. is to sell its unique, carefully researched, developed and manufactured Farm-to-Consumer products "the Nerium SkinCare "Product Line" to consumers.</p>

		<p>NI is managed by Jeff Olson, who Nerium SkinCare, Inc., trusted with the task and opportunity to distribute its unique first of its kind Nerium SkinCare cosmetic Product-Line.</p>

		<p>Unfortunately, since 2013 Jeff Olson has deviated from the intent and purpose of Nerium SkinCare, Inc., and he started developing and eventually distributing products not part of Nerium SkinCare Product-Line.</p>

		<p>These non-nerium products Jeff Olson has named Optimera. Recently, he has made the Optimera products look so identical to the Nerium Product-Line products, even many of the NI brand partners themselves have reported that the two product lines (Optimera and NeriumAD) are the same......THEY ARE NOT!</p>

		<p>Nerium SkinCare Inc., and its parent company Nerium Biotechnology, Inc., take great pride in the long history of research, development and investment in its farms, and human health science that has gone into its drug products to help mitigate diseases, and its anti-aging Nerium SkinCare Product-Line.</p>

		<p>From the 365 days a year effort which goes into the Nerium farms, to the quality control and careful manufacturing, consumers who use Nerium products can feel this extraordinary effort, integrity, and the quality of the unique ingredients and people behind Nerium Biotechnology and Nerium SkinCare.</p>

		<p>Nerium SkinCare Inc., has invested everything in assuring that the highest quality control and good manufacturing practices, go into each and every bottle of NeriumAD. Our Nerium history and the strong quality foundation of research and integrity should not be used to build and promote a knock-off brand.</p>

		<p>Though Nerium SkinCare Inc., has made numerous attempts to negotiate a solution with Mr. Olson, or to stop him from selling the non-Nerium Product Line, under the brand Nerium, he has refused and continues to develop, and sell non-nerium products under the Nerium brand name. Nerium SkinCare Inc., wants Mr. Olson to STOP selling the non-nerium products under the Nerium name, this is why Nerium SkinCare Inc., has filed the lawsuits.</p>

		<p>It is important for our Brand Partners and customers to understand the difference between the authentic NeriumAD manufactured products, and the knock-off "Optimera formulas" made to look like the Nerium Product-Line.</p>




